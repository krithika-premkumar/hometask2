package fifthtask;

public class Car {
int id;
String Model;
long year;
String color;
double price;
String Registration_number;
Car(int id, String Model, long year, String color, double price, String Registration_number)
{
	this.id = id;
	this.Model = Model;
	this.year = year;
	this.color = color;
	this.price = price;
	this.Registration_number = Registration_number;
}
}
